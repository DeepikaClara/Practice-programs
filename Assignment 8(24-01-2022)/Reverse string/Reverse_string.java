package basics;
public class Reverse_string   //class name
{
	public static void main(String args[])    //mainfunction
	{
String A="Catch your Dreams";
System.out.println("String to be reversed is: "+A);
System.out.print("Reversed String is: ");
int size=A.length();       //  method name
{
for(int i=0;i<size;i++)    //for loop
{
System.out.print(A.charAt(A.length()-i-1));
}
}
}
}