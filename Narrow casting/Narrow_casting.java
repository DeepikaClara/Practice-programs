package basics;

public class Narrow_casting 
{
public static void main(String args[])  //main function
{
	float a=10.46f;
	double b=7.89d;
	int c=(int)a;    //narrow casting[float to int]
	short d=(short)b;   //narrow typecasting [double to short]
	System.out.println(c);
	System.out.println(d);
}
}
